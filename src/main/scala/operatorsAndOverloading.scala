object operatorsAndOverloading extends App {

  println(4 + 6)
  println(4.+(6))
  println(6 % 4)
  println(6 > 4)
  println(8 < 7)

  val t = true
  println(t)

  val f = false
  println(f)

  val test = t || f
  println(test)

  println(!f)
  println(1 | 2)

  val num = 3 + 5L
  println(num)

  println(5.55d + 2.2f)

}
